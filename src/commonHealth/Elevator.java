package commonHealth;

public class Elevator {
	private int floor;
	private int lift_number;
	private boolean status;
	public int getFloor() {
		return floor;
	}
	public void setFloor(int floor) {
		this.floor = floor;
	}
	public boolean isStatus() {
		return status;
	}
	public void setStatus(boolean status) {
		this.status = status;
	}
	public int getLift_number() {
		return lift_number;
	}
	public void setLift_number(int lift_number) {
		this.lift_number = lift_number;
	}
}
